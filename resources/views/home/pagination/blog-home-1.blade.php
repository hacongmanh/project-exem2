                    <h2 class="mb-3"> TÌNH THƯƠNG LÀM THĂNG HOA CUỘC SỐNG</h2>
                    <p style="font-weight: bold"> Cuối năm 2008, People weekly magazine lần đầu tiên đưa hình ảnh một con chó lên trang bìa báo. Đây là một chuyện chưa từng xảy ra cho tờ tạp chí mang tên "People".</p>
                    <p>Ngay đến nhật báo nghiêm túc nhất, tờ Washington Post cũng đã có những lời ca ngợi như sau:  "Sự ra đời của con vật là một động lực mạnh mẽ cho đất nước đang gặp nhiều sự xáo trộn này. Bản thân con vật mang nhiều nỗi bi thương nhưng nó đã mạnh dạn đứng dậy và nhìn đời với sự bao dung và hạnh phúc. Tên của nó là Niềm Tin (Faith), đó là một con chó đứng bằng hai chân sau và có thể hiên ngang tiến bước như tất cả mọi con người trên xã hội này."</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347209528862/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-01.jpg" alt="">
                    <p>Con vật này ra đời vào giữa đêm Noel năm 2002. Khác với những chị em của nó có được bốn chân bình thường, nó chào đời với tật bệnh bẩm sinh. Hai chân trước nhỏ bé co rụt không sử dụng được, chỉ có hai chân sau may mắn lành lặn. Ngay chính mẹ nó cũng ruồng bỏ không chịu cho con vật đáng thương này đụng đến bầu sữa. Chủ nhân của con chó tật nguyền nghĩ rằng con vật khốn khổ không thể nào tồn tại ở trên thế gian thực tiễn nầy nên đã vứt bỏ nó vào thùng rác. Thời may, một người có tấm lòng là Jude Stringfellow đã mang con vật này về nuôi dưỡng. Bà Stringfellow giữ vững niềm hy vọng rằng con vật khốn khổ này sẽ một ngày nào đó đứng dậy để đối diện với cuộc đời cho nên bà đã đặt tên cho nó cái tên Faith tức "Niềm Tin".</p>
                    <img style="width: 195px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347209607995/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-02b.jpg" alt="">
                    <img style="width: 350px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347209638306/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-03b.jpg" alt="">
                    <p> Bà Stringfellow đặt làm một cái skateboard để Faith có thể tựa phần trên của cơ thể lên đó rồi dùng hai chân sau làm động lực đẩy cả cái xe về phía trước</p>
                    <img style="width: 200px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347209746804/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-04b.jpg" alt="">
                    <p>Bà Stringfellow dùng một muỗng peanut butter làm mồi nhữ để dạy cho Faith đứng dậy. Rồi sau đó, đến những bước đi như một đứa bé tập đi.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347209818713/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-05b.jpg" alt="">
                    <p>Sáu tháng sau, Faith đã có thể chạy nhảy bất cứ nơi nào, cô chó cái này rất thích ra công viên để được nô đùa cùng đàn ngỗng trời.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347209875272/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-06b.jpg" alt="">
                    <p>Faith cũng thích tháp tùng cùng bà Stringfellow di dạo khắp mọi nơi.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347209922626/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-07b.jpg" alt="">
                    <p> Bất luận đến một nơi nào, Faith cũng trở thành trọng tâm chú ý của mọi người. Ai nấy đều tỏ về trìu mến thương yêu Faith.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347209985498/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-08b.jpg" alt="">
                    <p> Đứng lên bằng hai chân là một cảm giác tuyệt vời nên Faith không từ nan bất cứ một nơi nào nào cần đến sự có mặt của cô nàng</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210036790/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-09b.jpg" alt="">
                    <p>Faith thích nô đùa cùng trẻ con, lúc nào Faith cũng có cặp mắt ướt át nhân từ như một bà mẹ</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210080664/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-10b.jpg" alt="">
                    <p> Faith được mang đến bệnh viện để an ủi những người lâm trọng bệnh. Sự xuất hiện của Faith đã khiến cho nhiều người tăng thêm sức chịu đựng. Những bệnh nhân giữ vững được niềm tin và tăng thêm lòng can đảm để chống lại với bệnh tật.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210140658/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-11b.jpg" alt="">
                    <p> Faith đã không ngừng mang tiếng cười đến cho mọi người.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210185450/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-12b.jpg" alt="">
                    <p>Những gì Faith làm được, chưa chắc con người chúng la có thể làm được. Bài học chính mà Faith đã giảng dạy cho loài người rằng:  "Không thể đi được bằng bốn chân thì ráng tập đứng dậy và di chuyển bằng hai chân. Đứng dậy được ta sẽ trông xa thấy rộng, tầm nhìn sẽ bát ngát bao la hơn. Mất hai chân trước, nhưng ngược lại lòng dũng cảm của Faith cũng to rộng tương tự như đôi cánh của Đại Bàng.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210232473/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-13b.jpg" alt="">
                    <p> Một con chó có thể không hiểu được đạo lý làm người, thế nhưng con người có thể từ bài học của con chó hai chân để nghiền ngẫm lại chính bản thân họ. Cũng chính vì vậy mà cô chó này đã được tạp chí People mang lên hình bìa.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210281073/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-14b.jpg" alt="">
                    <p>Bà Jude Stringfellow đã viết một quyển sách "With a little Faith" diễn tả những kinh nghiệm trong giai đoạn dạy cho Faith đứng dậy.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210341772/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-15b.jpg" alt="">
                    <p> Bà còn thiết lập một website cho Faith, bạn sẽ không ngờ rằng mỗi tháng số lượt người xem đã lên hơn 75000 và mỗi ngày Faith nhận được trên 300 emails đến từ fans của cô.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210389299/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-16b.jpg" alt="">
                    <p> Hiện nay bà Stringfellow đang dự định tạm đình chỉ công việc dạy học của bà để mang Faith đi một tour thế giới, Mục đích của bà là muốn giới thiệu với nhân loại cái đẹp của tâm hồn có thể chiến thắng sự khiếm khuyết của cơ thể.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210439058/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-17b.jpg" alt="">
                    <p> Chúng ta hãy tin tưởng vào tình thương, bản thân của Faith chính là niềm tin, sinh mạng và phép lạ.</p>
                    <img style="width: 400px" src="https://sites.google.com/site/vuvandailang/_/rsrc/1347210488184/ngu-ngon/cau-chuyen-ve-mot-con-cho-hai-chan/Faith-18b.jpg" alt="">
                    <p>  Nếu bạn thích câu chuyện này, thì bạn hãy mang tình thương, niềm tin và sức mạnh của sinh mạng chia sẻ cùng thân nhân của bạn.

